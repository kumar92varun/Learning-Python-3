# Program to learn about Lists built-in data type
print('=== Creating lists ===')
x = list()		# Empty list
print(x)

x = []			# Again, an empty list
print(x)

x = ['a', 'b', 'c']
print(x)
print(type(x))

x = [-17.5, "Kilo", 49, 'V', ['ram', 5, 'echo'], 7, True, False, None]
print(x)
print(type(x[-1]))
print(type(x[-2]))
print(x[-5][-1])